class Calc():
    def __init__(self,num1,num2):
        self.num1 = num1 
        self.num2 = num2

    def add(self):
        return self.num1+ self.num2


calce = Calc(1,1)
calce1 = Calc(2,2)
print(calce.add())
print(calce1.add())

